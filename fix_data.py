def load_data(filepath):
    """Load data.js and parse the JavaScript-like structure."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    # Extract JSON from the JavaScript object
    # Find the start of the JSON object
    start = content.find('{')
    end = content.rfind('}') + 1
    json_str = content[start:end]

    data = json.loads(json_str)
    return data


def save_data(data, filepath):
    """Save data back to data.js, preserving the JavaScript wrapper."""
    json_str = json.dumps(data, ensure_ascii=False, indent=2)

    wrapper = "const QUESTION_DATA = \n" + json_str + "\n"

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(wrapper)


def is_short_explanation(exp):
    """Check if explanation is a short fragment (<30 chars) typical of answer key fragments."""
    if not exp or len(exp.strip()) == 0:
        return False  # Empty is handled separately
    length = len(exp.strip())
    if length < 30:
        # Check if it looks like an answer key fragment
        fragments = [
            "正确答案是", "正确答案", "答案是", "答案",
            "A：", "B：", "C：", "D：", "E：",
            "正确", "解析", "本题",
        ]
        for frag in fragments:
            if exp.strip().startswith(frag):
                return True
        # Also check pure short answers like "B：成纤维细胞"
        if len(exp.strip()) < 15 and '：' in exp:
            return True
    return False


